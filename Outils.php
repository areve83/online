          <div class="panel-body"><strong>Devenir un professionnel RGE pour les travaux de rénovation énergétique</strong><br>
            <i>Mis à jour le 09/07/2016</i><br>
            En tant que professionnel, vous trouverez ici tous les conseils et informations nécessaires à l’obtention de la mention RGE, afin d'être facilement identifiable par les maîtres d’œuvre. <br>
            Cette mention RGE Travaux concerne principalement les travaux de rénovation énergétique des particuliers pour les logements individuels ou collectifs.
            <br>
            Voici les 5 étapes à suivre :
          <ul>
              <li><strong>Étape 1</strong> : Choisir un signe de qualité</li>
              <li><strong>Étape 2</strong> : se former et former ses collaborateurs</li>
            Plusieurs possibilités s’offrent à vous en fonction des formations que vous avez déjà suivies.
            Dans votre établissement, au moins un de vos collaborateurs devra être formé.
            Des formations différentes sont disponibles en fonction de votre domaine d’activités (efficacité énergétique ou énergies renouvelables).</li>
              <li><strong>Étape 3</strong> : faire la démarche de qualification
            Vous devez maintenant vous rapprocher du ou des organismes retenus. Ils vous indiqueront la démarche à suivre pour l’obtention de la qualification.
            Les organismes de qualification vous vous demander plusieurs éléments afin d’attester des critères :
            
            légaux, administratifs et juridiques ;
            financiers ;
            techniques (ressources humaines, références de réalisation et moyens matériels).</li>
              <li><strong>Étape 4</strong> : obtenir son certificat et maintenir sa qualification
            Une fois la qualification ou certification obtenue, vous obtenez un certificat de qualification et votre entreprise est alors référencée auprès des particuliers sur le site : <a href="http://www.renovation-info-service.gouv.fr/trouvez-un-professionnel" target="_blank">www.renovation-info-service.gouv.fr</a><br>            
            La mention RGE est obtenue pour un délai de 4 ans. Le certificat est renouvelé tous les ans après contrôle de certains éléments (pérennité des moyens humains, techniques et financiers de l’entreprise).</li>
            
              <li><strong>Étape 5</strong> : réussir un contrôle de réalisation
            Un contrôle de réalisation sera effectué sur un chantier dans un délai de deux ans. Ce controle est réalisé par l'organisme de qualification ou de certification. Il vise à vérifier la conformité des travaux aux règles de l'art. 
                <a href="http://www.ademe.fr/entreprises-monde-agricole/labels-certifications/entreprise-batiment/devenir-professionnel-rge-travaux-renovation-energetique" target="_blank">http://www.ademe.fr/</a></li>
        </ul></div>
