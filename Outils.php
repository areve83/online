                        <br>
                        <h4 style="color:red;strong;text-align:center">Devenez Accompagnateur</h4>
<h4 style="strong">Pourquoi choisir Ma Maison BDM ?</h4>

Réaliser une Maison BDM c'est profiter de l'expérience de plus de 200 bâtiments reconnus BDM  dans la Région PACA. Des bâtiments qui ont choisi d'être durables et qui ont été pensés du début de la conception jusqu'à l'exploitation, pour valider des choix techniques et définir ce que devrait être un bâtiment durable en Région PACA.
Notre rôle est de vous accompagner tout au long de votre projet, en faisant valider les choix techniques par une commission d'experts bienveillants à votre service. BDM est là pour évaluer la pertinence globale de votre projet de construction ou de rénovation.<br>
Faire du BDM c'est :
<ul><li>Etre convaincu que l'on peut vivre confortablement en été sans climatisation, même dans le Sud de la France</li>
<li>S'intéresser au choix des matériaux, pour diminuer son empreinte carbone</li>
<li>S'assurer de la santé de sa famille, en prenant en compte les aspects de ventilation, d'éclairage et les finitions</li>
<li>Faire des choix simples et cohérents pour sortir des solutions classiques et pas toujours adaptées</li>
<li>Respecter l'environnement direct du bâtiment, le site, l'architecture régionale</li>
<li>Soutenir une autre vision du bâtiment, une vision durable !</li></ul>
<h4 style="strong">Avec une Démarche globale</h4>
Pour BDM, durable ne rime pas seulement avec réduction des consommations. Faire du durable, c'est aller plus loin c'est pour cela que nous proposons des objectifs à atteindre sur les 7 thèmes de la Démarche BDM : des outils de gestion de projets pour favoriser le bioclimatisme, minimiser l’impact des matériaux, réduire les consommations d’eau et d’énergie pour préserver le confort et la santé des occupants tout en tenant compte des enjeux sociaux et économiques.<br>Pour toute information, prenez contact avec <a href="mailto:bvion-dury@envirobatbdm.eu">Benoît Vion Dury.</a>
