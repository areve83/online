        	<div class="panel-body">7 signes bénéficient de la reconnaissance « RGE »
				<ul>
                <li>ECO Artisan (marque CAPEB et qualification Qualibat) repose sur une approche globale de la performance énergétique, qui ne s’appuie pas sur un seul métier mais sur l’interface indispensable entre les différents corps de métier.
                Les 4 qualifications délivrées par Qualit’ENR concernent les installateurs d’énergies renouvelables : Quali’Sol pour les installateurs de solaire thermique, Quali’PV pour les installateurs de solaire photovoltaïque, Quali’Bois pour les installateurs de bois énergie, Quali’Pac pour les installateurs de pompes à chaleur.</li>
                <li>Qualibat bénéficie de la mention RGE pour ses qualifications relatives aux énergies renouvelables (photovoltaïque, solaire thermique, PAC et bois) et à l’efficacité énergétique (bouquets de travaux, mention efficacité énergétique « travaux isolés » et « rénovation énergétique offre globale »). Qualifelec propose en option des qualifications accompagnées de la mention « économie d’énergie » ou de la mention » solaire photovoltaïque », pouvant accompagner une qualification en électrotechnique. Cela veut dire que le professionnel devra aussi posséder l’une de ces mentions.</li>
                <li>Cequami (certification NF Maison Rénovée accessible aux constructeurs, architectes, entreprises générales, artisans, bureaux d’étude) et Certibat (certification « Offre Globale Rénovation Energétique »)  bénéficient également – à titre transitoire – de la mention RGE. Elles s’adressent à un public qui n’est pas la cible principale de ce site.</li> 
                <li>Les Pros de la Performance énergétique (marque FFB et qualification Qualibat) permet de devenir RGE sur son activité  ou sur une offre globale d’amélioration énergétique.</li></ul>
