				<h4 class="roboto">Un peu de technique</h4>
				<p class="roboto">La thermographie aérienne consiste à embarquer à l’intérieur d’un avion ou d’un hélicoptère une caméra infrarouge reliée à un ordinateur portable équipé d’un logiciel qui permet une visualisation et un enregistrement en temps réel des images.
					<br>Cette technologie permet d’estimer le niveau de déperdition de chaque toiture de bâtiment, qu’il s’agisse d’une maison individuelle, d’un bâtiment collectif ou d’un équipement public.
					<br>Dans la pratique, elle nécessite des conditions climatiques et météorologiques particulières :
					<ul class="roboto">
						<li>une température au sol inférieure à 5°C ;</li>
						<li>une absence de soleil ;</li>
						<li>une absence de nuage ;</li>
						<li>des vents faibles (vitesse inférieure à 20km/h) ;</li>
						<li>un temps sec : pas de neige, de brouillard ou de précipitations 36h avant le vol.</li>
					</ul>
				</p>
