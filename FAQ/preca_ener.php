					<h4 class="panel-title" style="font-weight: bold;"><a data-toggle="collapse" data-parent="#accordion1" href="#collapse1">Qu'est-ce que la précarité énergétique ?</a></h4>
				</div>
				
				<div id="collapse1" class="panel-collapse collapse">
					<div class="panel-body">La loi portant engagement national pour l’environnement du 10 juillet 2010, dite loi Grenelle 2, énonce une définition légale de la précarité énergétique :<br>« Est en situation de précarité énergétique au titre de la présente loi une personne qui éprouve dans son logement des difficultés particulières à disposer de la fourniture d’énergie nécessaire à la satisfaction de ses besoins élémentaires en raison de  l’inadaptation de ses ressources ou de ses conditions d’habitat. »<br>La précarité énergétique résulte à la fois d'une contrainte sur les ressources du ménage et des caractéristiques du logement.<br>
					Plusieurs approches peuvent être envisagées :
						<ul><br>
						<li><strong>L’effort financier</strong></li>
						<p>Une première approche consiste à considérer comme en situation de précarité énergétique tout foyer consacrant plus de 10% de son revenu aux dépenses énergétiques. Le critère des 10% ne reflète toutefois qu’imparfaitement la réalité.</p>
						<li><strong>Les comportements de restriction</strong></li>
						<p>Cette restriction peut être appréhendée soit par des enquêtes déclaratives soit par un calcul de la différence entre dépense réelle et dépense théorique.</p>
						<li><strong>Le ressenti de l’inconfort</strong></li>
						<p>Enfin, la précarité énergétique peut être caractérisée par un indicateur subjectif tel que la sensation de froid.<br>Ainsi, dans l’Enquête Nationale Logement, l’INSEE prend en compte l’indicateur « avoir froid dans son logement en hiver » (La précarité énergétique, avoir froid ou dépenser trop pour se chauffer, INSEE).<br></p>
						</ul>
						
						<p><i>Source : Panorama énergies-climat 2014, Ministère de l’Écologie, du Développement durable et de l’Énergie</i></p>
						
