						<h3 class="header-3">Le Tarif de première nécessité (TPN) et le Tarif spécial de solidarité (TSS)</h3>
						<p>Afin d’atténuer l’impact des prix de l’énergie sur les ménages modestes, ont été mis en place des aides sous condition de ressources, le tarif de première nécessité (TPN) pour l’électricité et le tarif spécial de solidarité (TSS) pour le gaz.<br>Le TPN prend la forme d’une déduction forfaitaire modulée en fonction du nombre de personnes composant le foyer bénéficiaire et de la puissance souscrite. Il correspond à une réduction sur la facture comprise entre 71€ et 140€ par an et bénéficiait en février 2014 à 2 200 000 foyers. <br>Le TPN est financé par la contribution au service public de l'électricité (CSPE). En 2012, l’état a pris la décision de rendre automatique l’attribution de ces tarifs.
						</p>
						<p class="pmax">Pour qui ?</p>
						<p>Les personnes aux ressources permettant être bénéficiaire de la Couverture Maladie Universelle Complémentaire (CMU-C) ou de l’Aide pour une Complémentaire Santé (ACS) sont automatiquement éligibles aux Taris sociaux.<br>Ou si le revenu fiscal annuel de référence du foyer est inférieur à 2 175 euros par part (décret n° 2013-1031 du 15 novembre 2013).</p>
						<p class="pmax">Comment en bénéficier ?</p>
						<p>L’attribution des tarifs sociaux est, dans la majorité des cas, automatique.<br>Je reçois une attestation m’indiquant que j’en bénéficie, sauf opposition de ma part dans un délai de 15 jours. Et si l’attribution n’a pu se faire automatiquement, je reçois une attestation m’indiquant que je peux bénéficier du TPN/TSS et les modalités pour l’obtenir.<br>Pour plus d’informations sur les tarifs sociaux, je peux contacter, du lundi au vendredi, de 9h à 18h, les numéros verts (gratuits depuis un poste fixe) suivants : 
						<ul>
							<li>N° vert TPN : 0 800 333 123</li>
							<li>N° vert TSS : 0 800 333 124</li>
						</ul>
						<h3 class="header-3">Les aides financières du fonds de solidarité pour le logement (FSL)</h3>
