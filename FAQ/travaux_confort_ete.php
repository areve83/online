        <p>En climat littoral, où l’humidité est un facteur aggravant du stress thermique, les risques d’inconfort sont plus importants mais il n’est, en général, pas trop difficile de s’en préserver. Protections solaires efficaces et ventilation sont alors les outils majeurs du confort.<br>
        Il convient à la fois de :
        <ul><p>
            <li>Limiter les apports solaires (protections solaires fixes et/ou mobiles) ;</li>
            <li>Limiter les apports thermiques externes (pas de ventilation pendant la journée) ;</li>
            <li>Limiter les apports thermiques internes, notamment ceux de l’éclairage ;</li>
            <li>Favoriser le stockage des pointes de chaleur (rôle de l’inertie thermique) ;</li>
            <li>Favoriser l’évacuation nocturne de la chaleur par l’aération ou la ventilation nocturne.</li>
        </ul>
            <p>Ainsi, dans un bâtiment bien conçu, à forte inertie, bien isolé et équipé de protections solaires efficaces, l’occupant qui saura gérer les protections solaires et une ventilation judicieuse, obtiendra des conditions de confort satisfaisantes même par forte chaleur.<br>
            La conception du bâtiment joue donc un rôle important pour le confort d'été, or la majorité des logements de demain étant déjà construits aujourd'hui, les solutions passeront donc par une amélioration de l'enveloppe des bâtiments existants.<br>
            Dès que l'aspect extérieur du bâtiment doit être modifié, il faut :
            <ul>
                <li>Si l'on est en copropriété, se référer au règlement de copropriété qui indiquera ce qui est réalisable et ce qui ne l'est pas. Si plusieurs copropriétaires ont le même problème, il est intéressant de le régler globalement à l'échelle collective.</li>
                <li>Déposer une déclaration préalable de travaux auprès du Service d'Urbanisme de votre commune, selon les modalités en vigueur du Corde de l'Urbanisme.</li>
