			<!--montants-->
			<div class="panel-heading">
				<h4 class="panel-title" style="font-weight: bold;"><a data-toggle="collapse" data-parent="#accordion1" href="#collapse3">Quels sont les montants de ces CEE ?</a></h4>
			</div>
			<div id="collapse3" class="panel-collapse collapse">
				<div class="panel-body">Par souci de visibilité, les opérateurs obligés ou éligibles proposent généralement des aides forfaitaires en fonction du type de travaux. <br>Ces aides varient d'un opérateur à un autre, et ne sont pas forcément financières, elles peuvent se présenter sous la forme de conseils, de services d'accompagnement, de remise sur la facture des travaux, de remise sur votre facture d'énergie, d'un prêt à taux bonifié, de bons d'achat dans un magasin, de cadeaux ou d'aides financières directes…
					<hr>Par exemple : Monsieur ALBERT habite une maison de 100m² située à Fayence.<br> Il fait réaliser l’isolation de la toiture (combles perdus) sur la totalité de la surface.<br>Cette action génère, selon la fiche d'opération standardisée BAR-EN-01 : 66 000 kWh cumac.<br>Selon la cotation constatée sur <a href="https://www.emmy.fr/front/accueil.jsf"> www.emmy.fr</a> ces travaux peuvent être valorisés financièrement à hauteur de 107 € (0,163 c€/kWh cumac – juin 2016).<br>Il obtient 100 € sous forme de bon d'achat sur sa carte de fidélité auprès de son supermarché (ce supermarché est aussi fournisseur d’énergie, car il vend du carburant).
				</div>
			</div>
