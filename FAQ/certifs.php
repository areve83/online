			<div class="panel-heading">
				<h4 class="panel-title" style="font-weight: bold;"><a data-toggle="collapse" data-parent="#accordion1" href="#collapse4">Qu'est-ce-qu'un obligé ?</a></h4>
			</div>
			<div id="collapse4" class="panel-collapse collapse">
				<div class="panel-body">Un obligé peut être :
					<ul>
						<li>un artisan : certaines entreprises ont des partenariats avec des fournisseurs d'énergie qui leur reversent alors une compensation financière en échange des certificats d'économie d'énergie correspondant aux travaux que l'entreprise aura réalisés chez son client.<br>Renseignez-vous afin de savoir si l'entreprise qui réalise les travaux chez vous compte valoriser les certificats ou non, et si oui à quel prix ?</li>
						<li>un fournisseur d'énergie : certains fournisseurs d'énergie proposent des aides directement accessibles aux particuliers, sous différentes formes (chèques travaux, réduction sur l'achat de combustible, prêt à taux réduit, conseil gratuit, etc.). <br>Dans certains cas, ces fournisseurs peuvent travailler avec leur propre réseau de professionnels et ne pas laisser le choix de l'artisan.</li>
						<li>un courtier : vous pouvez envoyer une copie de vos devis à un courtier qui négociera pour vous et au meilleur prix vos CEE auprès de différents fournisseurs d'énergie et vous reversera ensuite une partie de la compensation financière.</li><br><i>Source : ADIL 34</i>
                    </ul>
				</div>
			</div>
