				<div class="panel-body">Une fois que vous avez vérifié que les travaux envisagés sont éligibles au dispositif des certificats d'économies d'énergie (Cf. question « Quels sont les travaux éligibles ? »), vous devez chercher un obligé par type de travaux éligible.
				<br>L'obligé doit obligatoirement justifier du fait que son intervention, antérieure à la réalisation des travaux, vous a effectivement incité à faire des économies d'énergie. Cette contribution incitative peut être réalisée directement par un fournisseur d'énergie ou par un installateur qui lui est contractuellement lié.
				<br>Contractualisez avec l'obligé choisi et faites réaliser les travaux. Les travaux terminés, vous fournissez alors à l'obligé les documents (factures, attestation de fin de travaux, etc.) qui lui permettront de valoriser ces travaux en certificats d'économies d'énergie auprès des services instructeurs de l'Etat.
               <br>Si vous pouvez obtenir plusieurs CEE parce que vous réaliser plusieurs types de travaux éligibles, vous pouvez choisir un ou plusieurs obligé(s), à votre convenance.
