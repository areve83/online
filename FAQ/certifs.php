				<h4 class="panel-title" style="font-weight: bold;"><a data-toggle="collapse" data-parent="#accordion1" href="#collapse2">Quels sont les travaux éligibles ?</a></h4>
			</div>
			<div id="collapse2" class="panel-collapse collapse">
				<div class="panel-body">Des fiches d’opérations standardisées, définies par arrêtés, ont été élaborées pour faciliter le montage d’actions d’économies d’énergie. Elles définissent les montants forfaitaires d’économies d’énergie CUMAC (cumulées et actualisées), et sont disponibles en <a href="https://www.ecologique-solidaire.gouv.fr/operations-standardisees#e4" target="_blank"> cliquant ici.</a><br><br>Vous avez droit à 1 CEE par type de travaux éligible.
				</div>
			</div>
