	<div class="container">
		<p>Si vous envisagez de réaliser des travaux d’économies d’énergie dans votre habitation, vous pouvez financer une partie de ceux-ci grâce aux Certificats d’Economies d’Energie (CEE). Les CEE ont été mis en place dans le but d’obliger les fournisseurs d’énergie à promouvoir des travaux d’économies d’énergie auprès de leurs clients (particuliers, collectivités, etc.), en apportant par exemple un soutien financier, technique ou autre. Un fournisseur d’énergie qui ne répond pas à cette obligation est pénalisé financièrement par les pouvoirs publics. Dans la pratique, certains travaux de rénovation engendrent des CEE susceptibles d’être achetés par des fournisseurs d’énergie. Plus les économies d’énergie générées par les travaux sont conséquentes, plus le montant de CEE est élevé. Cette aide est donc particulièrement intéressante pour les copropriétés.</p>
