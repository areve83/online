				<h4 class="panel-title" style="font-weight: bold;"><a data-toggle="collapse" data-parent="#accordion1" href="#collapse1">Qui y est éligible ?</a></h4>
			</div>
			<div id="collapse1" class="panel-collapse collapse">
				<div class="panel-body">Les CEE sont mobilisables, que vous soyez propriétaire occupant, bailleur, locataire ou occupant à titre gratuit, si vous envisagez de réaliser des travaux d’économies d’énergie dans votre habitation (résidence principale ou secondaire) construite depuis plus de deux ans.<br>Les CEE "précarité" ne peuvent être reçus que pour les travaux réalisés en résidence principale.
				</div>
			</div>
