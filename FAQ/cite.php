			<h3 class="header-3">Le Crédit d'Impôt Transition Energétique</h3>
		<div class="row">
			<p class="pmax">Les évolutions du CITE pour 2018</h2>
				<p>Le projet de loi de finances (PLF) pour 2018 maintient le CITE jusqu'au 31 décembre 2018 et restreint la liste des équipements éligibles. Le texte a été adopté le 21 novembre par l'Assemblée nationale dans une version modifiée.<br> En particulier, l'Assemblée a adopté l'amendement n° II-1904, qui prévoit les évolutions suivantes :
					<ul>
						<li>les chaudières à fioul sont exclues au 1er janvier 2018, sauf celles respectant des critères de performance renforcés (définis par arrêté), qui bénéficieront d'un taux de 15% jusqu'au 30 juin 2018</li>
						<li>le remplacement d'un simple vitrage par un double vitrage bénéficiera d'un taux de 15% jusqu'au 30 juin 2018</li>
						<li>les volets isolants et portes d'entrée sont exclus du CITE dès le 1er janvier 2018</li>
						<li>le CITE est étendu à certains frais d'équipements de raccordement à des réseaux de chaleur ou de froid, ainsi qu'à la réalisation d’un audit énergétique</li>
					</ul>
					Il est prévu que ces modifications ne s'appliquent pas aux contribuables ayant accepté un devis et versé un acompte avant ces dates.<br> En 2019, le CITE devrait être transformé en une prime, versée lors de la réalisation des travaux.</p>
