				<h4 class="panel-title" style="font-weight: bold;"><a data-toggle="collapse" data-parent="#accordion1" href="#collapse9">Quelles mentions doivent figurer sur les factures des travaux ?</a></h4>
			</div>
			<div id="collapse9" class="panel-collapse collapse">
				<div class="panel-body">Pour bénéficier du crédit d'impôt, les factures des entreprises, que le contribuable doit présenter à la demande de l'administration, doivent comporter les mentions suivantes :<br>
					<ul>
						<li>l'adresse de réalisation des travaux</li>
						<li>la nature des travaux.</li>En cas de travaux de natures différentes réalisés par la même entreprise, la facture ou l’attestation, selon le cas, doit comporter le détail précis et chiffré des différentes catégories de travaux effectués permettant d'individualiser d'une part, les équipements ouvrant droit au crédit d'impôt, d'autre part, ceux exclus du champ de cet avantage fiscal. En outre, à la demande des services fiscaux, les contribuables doivent fournir tout document permettant d'apprécier la nature et la consistance des travaux exécutés
						<li>la désignation et le prix unitaire des équipements, matériaux ou appareils éligibles</li>
						<li>le cas échéant, les normes et critères techniques de performance minimaux (Cf question "Quelles sont les caractéristiques techniques à respecter?").</li> A défaut de la mention exacte, sur la facture, des critères techniques de performance, la notice établie par le fabricant de l'équipement ou une attestation de ce dernier mentionnant le respect de ces critères peut être admise à titre de justification
						<li>la date du paiement de la somme due au principal et, selon le cas, des différents paiements dus au titre d’acomptes</li>
						<li>dans le cas de l'acquisition et de la pose de matériaux d'isolation thermique des parois opaques, la surface en mètres carrés des parois opaques isolées, en distinguant ce qui relève de l'isolation par l'extérieur de ce qui relève de l'isolation par l'intérieur</li>
						<li>dans le cas de l'acquisition d'équipements de production d'énergie utilisant une source d'énergie renouvelable, la surface en mètres carrés des équipements de production d'énergie utilisant l'énergie solaire thermique</li>
						<li>les critères de qualification de l'entreprise (pour plus de précisions sur les modalités de justification, il convient de se reporter au BOI-IR-RICI-280-20-30).</li>
