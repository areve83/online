               <h2>Comment entretenir mon installation de ventilation ?</h2>
                <p>A la longue, une VMC s’encrasse, elle devient moins efficace, plus bruyante. Si elle est trop encrassée, elle peut même contribuer à dégrader la qualité de l’air qu’elle insuffle dans la maison. Pour éviter ces dysfonctionnements et assurer la longévité de votre installation, il faut régulièrement nettoyer ses composantes, et surveiller leur état.</p>
