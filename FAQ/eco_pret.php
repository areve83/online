			</div>
			<div id="collapse13" class="panel-collapse collapse">
				<div class="panel-body">L’éco-prêt à taux zéro peut également être mobilisé directement par le syndicat des copropriétaires pour financer les travaux d’économie d’énergie réalisés sur les parties communes de la copropriété ou les travaux d’intérêt collectif réalisés sur les parties privatives.<br>Le syndic de copropriété peut en effet souscrire un éco-prêt à taux zéro « copropriétés » pour le compte du syndicat des copropriétaires.<br>Au moins 75% des quotes-parts de l'ensemble de la copropriété doivent être comprises dans des lots affectés à l’usage d’habitation, utilisés ou destinés à être utilisés en tant que résidence principale. <br>Seuls les copropriétaires de logements utilisés ou destinés à être utilisés en tant que résidence principale peuvent participer à l’éco-prêt à taux zéro «copropriétés».<br>Les logements appartenant aux copropriétaires souscrivant au prêt ne doivent pas avoir déjà fait l’objet d’un éco-prêt à taux zéro individuel.<br>Les bâtiments faisant l’objet des travaux doivent avoir été achevés avant le 1er janvier 1990. De plus, un seul éco-prêt à taux zéro « copropriétés» peut être mobilisé par bâtiment.
