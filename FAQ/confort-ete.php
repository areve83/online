				<p>Le confort d'été est fonction de paramètres sur lesquels il est possible d'agir, pour réduire la surchauffe rendant inconfortable un bâtiment ou un logement. Ces paramètres sont liés à la conception et la gestion du bâtiment, et à certains éléments physiologiques. Le confort d'été passe par la maîtrise de ces paramètres sans avoir forcément recours à la climatisation.<br>Les différentes paramètres influant sur le confort thermique sont :
					<ul>
						<li>La vitesse de l'air et sa température</li>
						<li>La température des parois</li>
						<li>Le rayonnement solaire sur l'occupant</li>
						<li>La tenue vestimentaire</li>
						<li>L'activité exercée</li>
						<li>L'âge et l'état de santé</li>
						<li>etc...</li>
					</ul>
					<p><b>Différentes sources d'apports de chaleur :</b> La principale donnée à considérer est la température intérieure du logement. Celle-ci résulte des flux de chaleur provenant de l'extérieur ou de l'intérieur du logement :
						<ul>
							<li><b>Apports externes :</b>En été, la chaleur pénètre depuis l'extérieur avec le rayonnement solaire direct, la transmission de chaleur par les parois (murs, fenêtres, toit) et les entrées d'air chaud (défauts d'étanchéité et ventilation).</li>
							<li><b>Apports internes :</b>pour autant, les apports internes ne doivent pas être négligés : il s'agit du fonctionnement de tous les appareils électriques (éclairage, réfrigérateur, téléviseur, etc.) et de l'énergie de cuisson, qui se convertissent en chaleur dans le logement. La présence d'occupants est également source de chaleur.</li>
						</ul>
						</li>
						<p>Trois moyens d'évacuer la chaleur :
							<ul>
								<li>Par l'entrée d'air frais (ventilation nocturne)</li>
								<li>En puisant la fraîcheur dans le sous-sol</li>
								<li>En humidifiant l'air</li>
						</p>
						</ul>

