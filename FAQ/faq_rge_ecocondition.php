 <!--Quelles exigences-->
      		<div class="panel-heading">
        	<h4 class="panel-title" style="font-weight: bold;"><a data-toggle="collapse" data-parent="#accordion1" href="#collapse6">Quelles exigences doit respecter une entreprise RGE ? Quelles sont les sanctions en cas de non-respect de ces exigences ?</a></h4>
        	</div>
      		<div id="collapse6" class="panel-collapse collapse">
                <div class="panel-body">
                En recourant à une entreprise titulaire d’un signe de qualité « RGE » correspondant aux travaux à réaliser, vous êtes assuré(e) que :<ul>
					<li><strong>L’entreprise dispose de références et d’un volume d’activité</strong><ul>
					<li>au moins deux références attestées pour la première qualification (sur les deux dernières années) ;</li>
					<li>possibilité d’une qualification probatoire qui ne peut pas excéder deux ans uniquement en matière de qualification et en l’absence de références ;</li>
					<li>présentation tous les deux ans d’au moins deux références.</li></ul>
					<li><strong>L’entreprise respecte les critères légaux et administratifs suivants :</strong></li><ul>
					<li>le professionnel est inscrit au Registre du Commerce et des Sociétés ou au Répertoire des Métiers ;</li>
					<li>le professionnel n’est pas en état de liquidation judiciaire ou de cessation d’activités ;</li>
					<li>les dirigeants de l’entreprise n’ont pas fait l’objet d’une interdiction de gérer ou d’une décision de faillite personnelle ou que l’entreprise n’appartient pas à une entreprise dont le siège social est situé dans un pays avec lequel tout commerce est interdit ;</li>
					<li>l’entreprise est en règle avec les obligations relatives au paiement des cotisations sociales ;</li>
					<li>l’entreprise est en règle avec les obligations relatives au paiement des impôts et taxes ;</li>
					<li>l’entreprise dispose des assurances couvrant la responsabilité liée à l’exercice des activités concernées par la ou les certifications.</li></ul>
					<li><strong>Le(s) responsable(s) technique(s) maîtrise/maîtrisent les compétences suivantes :</strong></li><ul>
					<li>il connaît l’état du marché ;</li>
					<li>il connaît les technologies clés et les solutions d’amélioration de la performance énergétique ;</li>
					<li>il maîtrise l’approche globale énergétique ;</li>
					<li>pour chaque groupe de technologies : il connaît les technologies et les produits, leurs avantages et leurs limites ;</li>
					<li>il maîtrise la mise en œuvre des technologies et les interfaces avec les autres composantes du bâtiment ;</li>
					<li>il maîtrise l’entretien et la maintenance des équipements.</li></ul>
					<li><strong>L’entreprise respecte les conditions de sous-traitance des travaux :</strong></li><ul>
					<li>l’entreprise assure tout ou partie de la fourniture et de la pose des produits ou équipements ;</li>
					<li>l’entreprise assume l’entière responsabilité des travaux donnés en sous-traitance ;</li>
					<li>le sous-traitant doit être titulaire du signe de qualité correspondant aux travaux réalisés.</li></ul>
					<li><strong>L’entreprise a fait, ou fera l’objet d’un audit obligatoire</strong></li><ul>
					<li>réalisation d’un contrôle de réalisation sur chantier en cours ou achevé ;</li>
					<li>cet audit doit être réalisé au moins une fois sur la durée de validité de la qualification et au plus tard deux ans après sa délivrance ou son renouvellement ;</li>
					<li>cet audit a pour objet de contrôler la conformité aux règles de l’art des prestations réalisées.</li></ul>
					<br></ul>
					<strong>Sanctions en cas de non-respect des exigences</strong><br>
					L’organisme qui a délivré le signe de qualité « RGE » doit prévoir, pour les entreprises qualifiées, une procédure de traitement des réclamations émanant des clients des entreprises concernées. Pour les entreprises certifiées, il doit établir une échelle de sanctions applicables si ces dernières manquent à leurs obligations vis-à-vis de l’organisme, des clients ou de tiers. Il doit ainsi prévoir :<ul>
					<li>la suspension de la qualification ou de la certification notamment en cas de départ du (ou des) responsable(s) techniques(s), et le retrait en cas de non-remplacement dans un délai de 6 mois ;</li>
					<li>la suspension ou le retrait en cas d’absence d’actions correctives suite à une non-conformité lors des contrôles de réalisation ;
					la suspension ou le retrait en cas de réclamation fondée d’un tiers ;</li>
					<li>la suspension ou le retrait en cas de volume d’activité insuffisant (ex : moins de deux références sur les deux dernières années)</li></ul>
                </div>
