        	<h4 class="panel-title" style="font-weight: bold;"><a data-toggle="collapse" data-parent="#accordion1" href="#collapse2">Qu'est-ce qu'un professionnel RGE ?</a></h4>
        	</div>
            <div id="collapse2" class="panel-collapse collapse">
        <div class="panel-body"><p>La mention RGE, créée par les pouvoirs publics, accompagne la montée en compétence des entreprises pour assurer la qualité des travaux de rénovation énergétique. Elle permet d'identifier facilement les signes de qualité reconnus et les professionnels dont le savoir-faire est démontré.<br>
                 Une entreprise titulaire d'un signe de qualité "RGE", délivré par un organisme de qualification ou de certification, satisfait à des exigences de moyens et de compétences pour la réalisation de certains travaux d’économies d’énergies des logements. C'est le seul dispositif reconnu par l’Etat.<br>
                  Les signes de qualité délivrés par les organismes spécialisés sont conformes à un référentiel portant sur la reconnaissance des capacités professionnelles, techniques et financières de l’entreprise pour la conception et la réalisation des travaux.<br>
           Chaque qualification attribuée à une entreprise a une durée de 4 ans. Toutefois, pour en bénéficier, pendant 4 ans, l’entreprise doit répondre chaque année à un questionnaire par lequel elle fournit les informations administratives nécessaires à la mise à jour de son certificat annuel de qualification.</p>
            </div>
