<div class="panel-heading">
    <h4 class="panel-title" style="font-weight: bold;"><a data-toggle="collapse" data-parent="#accordion1" href="#collapse6">Que contient un audit énergétique réglementaire ?</a></h4>
</div>
<div id="collapse6" class="panel-collapse collapse">
    <div class="panel-body">
        <p>L'audit énergétique est réalisé pour l'ensemble de l'immeuble (parties communes et privatives) et comprend :
        <ul>
            <li>un descriptif des parties communes et privatives du bâtiment,</li>
            <li>une enquête auprès des occupants ou des propriétaires non occupants (évaluation des consommations énergétiques, confort thermique, utilisation et gestion des équipements, amélioration thermique),</li>
            <li>la visite d'un échantillon de logements sous réserve de l'accord de leurs propriétaires,</li>
            <li>l'estimation des quantités annuelles d'énergie effectivement consommées pour chaque catégorie d'équipements collectifs ainsi que les montants des dépenses annuelles correspondants,</li>
            <li>la mention du classement énergétique du bâtiment,</li>
            <li>la mention du classement des émissions de gaz à effet de serre du bâtiment,</li>
            <li>des préconisations visant à optimiser l'utilisation, l'exploitation et la gestion des équipements collectifs,</li>
            <li>des propositions de travaux améliorant la performance énergétique du bâtiment,</li>
            <li>un rapport de synthèse à destination des copropriétaires pour leur permettre d'apprécier la qualité de leur immeuble et de juger de la pertinence ces travaux proposés.</li>
        </ul>
        La décision de réaliser un audit énergétique doit être votée à la majorité simple (article 24) des voix exprimées par les copropriétaires présents et représentés lors d’une assemblée générale.<br> En cas de vote contre, une nouvelle proposition de DPE collectif doit être présentée devant l'assemblée générale. Par exemple, il est possible de trouver un autre professionnel si celui proposé pose problème pour des raisons de coût.</p>
