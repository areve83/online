        <p>L’audit global prend en compte l’état du/des bâtiments de la copropriété et les bureaux d’études thermiques ne sont pas des spécialistes des pathologies du bâti. C’est pour cela que seule une équipe pluridisciplinaire est en mesure de répondre aux conditions de compétences nécessaires à la réalisation d’un audit global.<br>Le plus souvent, un bureau d’étude thermique travaillera ainsi en binôme avec un architecte.<br>L'AREVE référence des partenaires locaux et qualifiés pour vous accompagner. <a href="../page_partenaires.php" target="_blank"> Liste des partenaires </a> ou <a href="../contact1.php" target="_blank">contactez-nous.</a></p>
