                <h4 class="panel-title" style="font-weight: bold;"><a data-toggle="collapse" data-parent="#accordion1" href="#collapse2">Que contient le Diagnostic Technique Global ?</a></h4>
            </div>
            <div id="collapse2" class="panel-collapse collapse">
                <div class="panel-body">
                    <p>Le diagnostic technique global (DTG) prévoit :
                    <ul>
                        <li>une analyse de l'état apparent des parties communes et des équipements communs ;</li>
                        <li>un état de la situation du syndicat des copropriétaires au regard de ses obligations légales et réglementaires ;</li>
                        <li>une analyse des améliorations possibles concernant la gestion technique et patrimoniale de l'immeuble (état de l'immeuble) ;</li>
                        <li>un diagnostic de performance énergétique de l'immeuble ou un audit énergétique ;</li>
                        <li>une évaluation sommaire du coût ainsi que la liste des travaux nécessaires à la conservation de l'immeuble (notamment sur les 10 prochaines années).</li>
                    </ul>
                    Le DTG permet d'assurer l'information des copropriétaires sur la situation générale de l'immeuble et d'envisager d'éventuels travaux mis en œuvre dans le cadre d'un plan pluriannuel de travaux.
                    </p>	
