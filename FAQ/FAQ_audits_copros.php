        <div class="panel-heading">
            <h4 class="panel-title" style="font-weight: bold;"><a data-toggle="collapse" data-parent="#accordion1" href="#collapse3">Pourquoi réaliser un audit global plutôt qu'un DTG ?</a></h4>
        </div>
        <div id="collapse3" class="panel-collapse collapse">
            <div class="panel-body">
                <p>L’audit global n’est pas obligatoire, cependant cette étude permet de réaliser un état des lieux des caractéristiques énergétiques et architecturales des bâtiments de la copropriété (équipements de chauffage, de production d’eau chaude, de ventilation, etc.).
                    <br>Il se compose de deux parties : l’audit énergétique et l’audit architectural. Il vous permet donc d'avoir l'avis de spécialistes de l'énergie et de spécialiste des pathologies du bâti.
                    <br>Sa réalisation vous permet donc de disposer d’un audit énergétique, pour être en conformité avec la réglementation en vigueur. A terme, le rapport de l’audit global vous permet d’être en capacité de décider l’adoption d’un scénario de travaux, et de connaître les gains potentiels pour la copropriété.
                    <br>Un audit global comprend :
                    <ul>
                        <li>l'analyse des consommations d’énergie ;</li>
                        <li>l'analyse des contrats d’entretien ou d’exploitation et de fourniture d’énergie ;</li>
                        <li>l'analyse du fonctionnement des installations ;</li>
                        <li>les possibilités d’amélioration et d’économies d’énergie ;</li>
                        <li>la performance énergétique de la copropriété ;</li>
                        <li>des préconisations chiffrées d’amélioration de cette performance.</li>
                    </ul>
                </p>
            </div>
