    <p>Transmis par l’air ou les structures d’un bâtiment, en provenance des activités extérieures, de nos voisins, de nous-même ou des équipements qui nous entourent, le bruit nous accompagne en permanence dans nos logements.<br>Le bruit peut se transmettre directement par les parois séparatives (mur, plancher, plafond, cloison, fenêtres). Il se transmet également de façon indirecte (ou latérale) par les parois non séparatives entre la source du bruit et l’endroit où on le perçoit. Les transmissions parasites profitent des imperfections localisées (boîtiers électriques...) et des défauts d’étanchéité à l’air (fissures dans les murs, absence de joints aux fenêtres, jonctions entre ouvrages...) : là où l’air passe, le bruit passe.<br><br>
    Connaître les règles de transmission du bruit vous permettra de mieux comprendre les différentes techniques d’isolation acoustique qui peuvent vous être proposées.<br><br>
        <strong>La loi de masse :</strong> plus c’est lourd, mieux ça isole. Autrement dit, à épaisseur égale, une cloison en béton isolera mieux qu’une cloison en carreaux de plâtre, car à volume égal, le béton est plus lourd que le plâtre.<br>
        <strong>Le principe « masse-ressort-masse » :</strong> il consiste à utiliser des parois doubles, comme des plaques de plâtre ou des cloisons en briques désolidarisées séparées par un espace rempli avec un isolant souple : laine minérale, de bois, de chanvre, etc. (voir p. 13), qui absorbe et dissipe l’énergie. En rénovation, cette solution permet un traitement efficace sans surcharger les structures et une épaisseur d’isolant moins importante.</p>

