                <h3 class="header-3">Il existe différents systèmes de ventilation :</h3>
                <h4>La ventilation naturelle</h4>
                <p>La ventilation des logements a longtemps été laissée aux soins des conduits de cheminée et des multiples défauts d’étanchéité des constructions. On ne contrôle alors ni la circulation de l’air, ni les déperditions de chaleur. Ce système est souvent trop efficace en hiver, pas assez en été, et peut être perturbé par l’action du vent. Si les grilles sont bouchées, volontairement ou non, il ne fonctionne pas. Mais bien conçu en fonction des conditions climatiques et des caractéristiques de la maison, il peut la ventiler de façon satisfaisante.</p>
                <h4>Les Ventilations Mécaniques Contrôlées (VMC) simple flux</h4>
                <p>L’air frais venant de l’extérieur traverse d’abord les pièces de séjour et les chambres, et est évacué des pièces de service par un groupe d’extraction comportant un ventilateur.</p>
                <i>VMC simple flux</i>
                Il existe les VMC simple-flux autoréglables, qui ont des débits d’air constants quelles que soient les conditions extérieures (vent, pluie) et intérieures (nombre d’occupants, humidité), et les VMC hygroréglables, qui voient leur débit d’air varier en fonction de l’humidité intérieure, ce qui permet de garantir l’évacuation plus rapide d’un air très humide tout en limitant les gaspillages (ventilation adaptée aux besoins).
                <h4>Les Ventilations Mécaniques Contrôlées (VMC) double flux avec récupération de chaleur. </h4>
                <p>Ce système limite les pertes de chaleur inhérentes à la ventilation : il récupère la chaleur de l’air vicié extrait de la maison et l’utilise pour réchauffer l’air venant de l’extérieur.<br>
                    Il est constitué d’un circuit d’insufflation d’air neuf qui filtre l’air extérieur, le préchauffe au niveau d’un échangeur de chaleur et le pulse grâce à un ventilateur dans les pièces principales par le biais de bouches d’insufflation. Un circuit de récupération d’air vicié dans les pièces de service aspire et filtre l’air dans la cuisine, la salle de bains et les WC. Sa chaleur est récupérée au niveau de l’échangeur et transmise au circuit d’air neuf. L’air extrait est ensuite évacué à l’extérieur.<br>
                    Cet équipement est plus coûteux qu’une VMC simple-flux et consomme plus d’électricité, mais il permet des économies de chauffage importantes en récupérant jusqu’à 70% (90% dans les systèmes haute performance) de la chaleur contenue dans l’air vicié extrait.
                    <h4>VMC double flux</h4>
                    <p>La récupération de chaleur par la VMC est particulièrement intéressante dans une maison bien isolée, avec une très bonne étanchéité à l’air, et qui nécessite peu de chauffage : une VMC double flux peut récupérer environ 1500kWh par an. L’économie réalisée est alors comprise entre 7 et 10% de la consommation de chauffage. La qualité de la mise en œuvre et de l’entretien sont primordiales pour garantir l’efficacité de ce type d’équipement.<br>
                        En collectif, la VMC-gaz évacue par le même réseau l’air vicié du logement et les produits de combustion d’une chaudière ou d’un chauffe-eau à gaz.
                        <h4>La Ventilation Mécanique Répartie ou Ponctuelle (VMR ou VMP)</h4>
                        <p>Elle est constituée d’aérateurs individuels placés dans les pièces de service. Elle fonctionne selon le même principe qu’une VMC (balayage de l’air depuis les pièces principales jusqu’aux pièces de service d’où il est rejeté, en passant sous les portes de communication). Ce système convient en rénovation, quand la pose d’une VMC est trop problématique. Il existe des modèles d’aérateurs silencieux et consommant peu d’électricité, qui peuvent éventuellement être asservis à l’interrupteur de la lumière.</p>
