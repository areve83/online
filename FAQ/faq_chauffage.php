				<div class="panel-body"> La puissance de chauffage d’un logement est directement proportionnelle à l’isolation ; plus il est isolé, moins la puissance de la chaudière devra être élevée, et moins l’installation vous coûtera cher. En cas de remplacement d’une chaudière, il convient donc de revoir son dimensionnement surtout si des travaux d’amélioration thermique (isolation, remplacement des vitrages, etc.) ont été réalisés.<br>
                        <b>Exemple :</b><br>>                    
                        Une famille de 4 personnes habite dans un appartement de 100 m² datant de 1965, qui n’a pas été rénovée depuis sa construction :<br>
                        <b>-> Puissance nécessaire au chauffage : 12 kW</b><br>
                        
                        Une autre famille de 4 personnes habite dans un appartement de 100 m² datant de 2005, avec une isolation correcte (conforme à la Réglementation Thermique 2005) :<br>
                        <b>-> Puissance nécessaire au chauffage : 5,4 kW</b>
                        <hr>                    
                        Pour déterminer au mieux la puissance du chauffage, il convient de faire appel à un bureau d’étude indépendant, qui calculera les déperditions du logement afin de dimensionner le système (cette étude coûte généralement moins de 400 €).
