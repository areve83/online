				<p>En climat littoral, où l’humidité est un facteur aggravant du stress thermique, les risques d’inconfort sont plus importants mais il n’est, en général, pas trop difficile de s’en préserver. Protections solaires efficaces et ventilation sont alors les outils majeurs du confort.
					<br>Il convient à la fois de :
					<ul>
						<li>Limiter les apports solaires (protections solaires fixes et/ou mobiles) ;</li>
						<li>Limiter les apports thermiques externes (pas de ventilation pendant la journée) ;</li>
						<li>Limiter les apports thermiques internes, notamment ceux de l’éclairage ;</li>
						<li>Favoriser le stockage des pointes de chaleur (rôle de l’inertie thermique) ;</li>
						<li>Favoriser l’évacuation nocturne de la chaleur par l’aération ou la ventilation nocturne.</li>
					</ul>
				</p>
				<p>Ainsi, dans un bâtiment bien conçu, à forte inertie, bien isolé et équipé de protections solaires efficaces, l’occupant qui saura gérer les protections solaires et une ventilation judicieuse, obtiendra des conditions de confort satisfaisantes même par forte chaleur.<br> La conception du bâtiment joue donc un rôle important pour le confort d'été. Voici plusieurs points à prendre en compte :
					<h3>Pour une construction neuve</h3>
					<a href="#optimiser">Optimiser la disposition des pièces</a><br>
					<a href="#eviter">Eviter les fenêtres de toiture</a><br>
					<a href="#protection">Les protections solaires fixes</a>
					<ul>
						<li>
							<h4 style="font-weight: bold;color: red">L'orientation</h4>
							<p>L’orientation Sud est à privilégier :
								<ul>
									<li>En été, une façade exposée au Sud reçoit le plus faible ensoleillement hormis les orientations proches du Nord</li>
									<li>C’est également la façade qui est la plus efficacement mise à l’ombre, grâce à des pare-soleil horizontaux</li>
									<li>En hiver, la façade Sud est celle qui reçoit le plus d’ensoleillement.</li>
									<li>En été, ce sont les façades orientées Est et Ouest qui reçoivent les ensoleillements les plus importants.</li>
								</ul>
								<p>En été, l’énergie solaire transmise par une baie orientée Ouest est plus forte que celle d’une baie orientée Sud. Qui plus est, elle pénètre dans le bâtiment au moment de la journée où sa température intérieure est la plus élevée !</p>
						</li>
						<img src="../img/Confort_d_ete_19.jpg" alt="confort été orientation" style="margin: auto;width: 80%"></img>
						<p>A l’échelle de la région, les vents dominants sont des vents forts :
							<ul>
								<li><b>mistral :</b> vent sec, de secteur Nord-Ouest</li>
								<li><b>vent Sud à Est,</b> humide, porteur de pluie.</li>
								<p>Le vent provoque des zones de pressions positives ou négatives par rapport à la pression atmosphérique : la chaleur intérieure aux bâtiments est dissipée au moyen de courants d’air créés par les différences de pression, notamment dues au vent.<br> La double ou triple orientation des façades permet l’aération transversale de jour comme de nuit. Les logements traversants doivent toujours être préférés en zone urbaine pour favoriser la ventilation sur façades opposées, avec des ouvertures appropriées, mais aussi une “perméabilité” intérieure.</p>
							</ul>
							<p>A noter que sur la bande littorale, les brises marines – brise de nuit, brise de jour – provoquent une ventilation très efficace :
								<ul>
									<li><b>le jour,</b> l’air marin “frais” et humide souffle en général de la mer vers la terre</li>
									<li><b>la nuit,</b> l’air souffle de la terre vers la mer, après s’être rafraîchi sous le ciel clair.</li>
								</ul>
							</p>
							<li>
								<h4 style="font-weight: bold;color: red" id="optimiser">Optimiser la disposition des pièces</h4>
								<p>Pour les logements collectifs, la recherche d’une forme compacte à double orientation opposée est préférable à des logements mono-orientation pour favoriser la ventilation et rechercher le confort des pièces au Nord pendant les périodes d’ensoleillement maximum.</p>
								<img src="../img/Confort_d_ete_7.jpg" style="margin: 10px;width: 60%"></img>
								<p>L’orientation d’une pièce est déterminée principalement par celle de ses ouvrants. Les solutions proposées doivent concilier la recherche de soleil l’hiver, et la minoration des apports solaires l’été.<br> La façade Sud d’un bâtiment est un bon emplacement pour les pièces à vivre pendant la journée :
									<ul>
										<li>l’hiver : bon captage solaire</li>
										<li>l’été : protection solaire simple par auvent, masque horizontal, végétaux, etc…</li>
										<li>en demi-saison : prolongement agréable vers l’extérieur.</li>
									</ul>
									<p>Les chambres seront utilement ouvertes entre l’Est et le Sud, afin de bénéficier d’un bon ensoleillement l’hiver, et d’être à une température acceptable en été, dès le début de la nuit. Il est conseillé d’éviter l’orientation Ouest pour les chambres.<br>L’ajout d’espaces tampons (garages au Nord, vérandas au Sud) permet de valoriser les apports de chaleur dus au soleil tout en se protégeant du froid hivernal.</p>

									<img src="../img/Confort_d_ete_6.jpg" style="margin: auto;width: 50%"></img>
									<li>
										<h4 style="font-weight: bold;color: red" id="eviter">Eviter les fenêtres de toiture</id></h4>
										<p>Attention au soleil d’été, source d’éblouissement et de surchauffes très inconfortables. Il est conseillé d’éviter les fenêtres de toit, quelle que soit d’orientation, et notamment pour les toits à faible pente/inclinaison, très présents en Provence.</p>
										<img src="../img/Confort_d_ete_7.jpg" style="margin: auto;width: 50%"></img>
									</li>
									<li>
										<h4 style="font-weight: bold;color: red" id="protection">Les protections solaires fixes</id></h4>
										<p>En été, un bâtiment mal protégé du soleil est le siège de surchauffes, donc de conditions thermiques inconfortables. La conception d’une protection solaire estivale efficace est fondamentale pour qu’un bâtiment soit confortable et performant.</p>
										<ol>
											<li><b>Ajout d'une pergola en façade Sud :</b>
												<br>
												<p>La pergola protège le rez-de-chaussée de la façade Sud. La protection solaire apportée est variable selon la période de l’année. L’efficacité d’une pergola est souvent remarquable : une réduction du temps d’inconfort en période estivale de plus de 50% au rez-de-chaussée et de 40 à 90% à l’étage. Son impact est donc sensible sur l’ensemble du logement pour un coût relativement réduit.</p>
											</li>
											<li><b>Pare soleils extérieurs fixes</b>
											</li>
											<img src="../img/Confort_d_ete_10.jpg" style="width: 40%;margin: 10px"></img>
											<p>La « casquette » est constituée d’une avancée au-dessus de la surface réceptrice (auvent, débord de toiture, balcon, etc.). L’occultation est bonne l’été pour les orientations Sud-Est à Sud-Ouest, mais très faible à l’Est et à l’Ouest (soleil rasant). L’hiver, la casquette laisse passer le soleil quelle que soit son orientation (soleil plus bas en hiver).<br>La « loggia » combine les pare-soleil horizontaux et verticaux. La protection solaire est bonne l’été, du Sud-Est au Sud-Ouest, et moyenne toute l’année à l’Est et à l’Ouest.</p>
											<li><b>Les protections solaires mobiles</b>
											</li>
											<p>En été, un bâtiment mal protégé du soleil est le siège de surchauffes, donc de conditions thermiques inconfortables. La conception d’une protection solaire estivale efficace est fondamentale pour qu’un bâtiment soit confortable et performant.</p>
											<b>La végétation à feuilles caduques :</b>
										
											<p>La végétation à feuilles caduques fait partie de ce type de protection, très majoritairement implantées en prolongement direct des bâtiments (vélum de terrasse par exemple). Leur efficacité est optimale du côté Sud des bâtiments, secteur où les contraintes solaires estivales ne sont pas les plus fortes : le soleil y est plus haut sur l’horizon et l’énergie reçue plus faible que pour les expositions Est et Ouest.</p>
											<li><b>Les protections intérieures</b>
											</li>
											<p>Les protections intérieures type stores ou rideaux même opaques sont assez peu efficaces sur le plan thermique. En effet, lorsque le rayonnement solaire a traversé le vitrage, il se trouve partiellement piégé et échauffe l’air entre la vitre et la protection. Ces protections intérieures sont surtout utiles pour lutter contre l’éblouissement. Elles doivent être associées à des protections extérieures. La couleur des protections intérieures opaques doit être claire pour gagner en efficacité. Les volets intérieurs en bois, traditionnels en Provence, relativement isolants, présentent une assez bonne efficacité vis-à-vis des apports solaires.</p>
											<li><b>L'Isolation</b>
											</li>
											<ul>
												<li><b>La toiture</b>
												</li>
												<p>La durée d’exposition de la toiture et son importance relative l’amènent à être à l’origine d’une part importante des apports thermiques extérieurs (les apports à travers la toiture peuvent représenter 30 à 50% des apports totaux dans le cas d’un bâtiment d’un seul niveau). Les locaux situés sous toiture sont donc toujours plus inconfortables que les locaux des autres niveaux en raison des apports supplémentaires parvenant par cette voie.<br> Il est donc prioritaire de réduire ces apports solaires, qui bien que moins apparents que ceux parvenant à travers les vitrages verticaux, sont quantitativement importants.<br> Plusieurs solutions permettent d’assurer une bonne protection :
													<ul>
														<li><b>Les toits “ parasols ” ou “ surtoitures ”.</b> Cette solution n’est valable que si la surtoiture est fortement ventilée sur tout le périmètre suivant la formule</li>
														<p>Surface totale d’ouverture / Surface de toiture > 0,15</p>
														<img src="../img/Confort_d_ete_25.jpg" style="margin: 10px; width: 40%"></img><br>
														<b>Avantages de la surtoiture</b>
														<ul>
															<li>Pas de surchauffe directe des plafonds</li>
															<li>Gain de lusieurs degrés en saison d'été (3 à 4°)</li>
															<li>Esthétique intéressante en architecture contemporaine.</li>
														</ul>
														<b>Inconvénients</b>
														<ul>
															<li>Surcoût</li>
															<li>Tenue au vent</li>
															<li>Maintenance de l'espace ventilé</li>
														</ul>
														<li><b>Les toitures avec combles ventilés (toitures froides)</b>
															<br>
															<p>Les toitures avec combles fortement ventilés sont préférables. Les plafonds sont isolés thermiquement pour le confort d’hiver, l’isolant étant dans le comble pour conserver au maximum l’inertie intérieure.<br>Les combles sont à surventiler en été. Toutefois, il faut équiper les orifices de ventilation avec des grillages anti-animaux. Il est par ailleurs recommandé d’obturer la majeure partie de ces orifices en hiver.</p>
														</li>
