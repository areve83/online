<div class="panel-heading">
    <h4 class="panel-title" style="font-weight: bold;"><a data-toggle="collapse" data-parent="#accordion1" href="#collapse4">Quelles solutions existent pour isoler ma toiture ?</a></h4>
</div>
<div id="collapse4" class="panel-collapse collapse">
<div class="panel-body">
<p>Il existe différentes techniques d’isolation, en fonction du type de toiture et de l’utilisation qui est faite des pièces. L’isolation par le plancher des combles perdus est généralement moins onéreuse et permet de réduire le volume chauffé ; c’est donc la solution à privilégier dans le cas où vous n’avez pas prévu d’aménager les combles. Dans le cas où l’isolant est placé directement sous la toiture, la pose d’un écran de sous-toiture doit être prévue pour prévenir les situations d’infiltration d’eau.</p>
<img class="img-responsive center-block" src="../img/isolation_toiture.png" alt="isolation toiture" style="max-width: 500px">
<p>Pour la toiture comme pour les murs, il est possible d’isoler par l’extérieur, en utilisant la méthode dite « Sarking » :</p>
<img class="img-responsive center-block" src="../img/sarking.png" alt="isolation toiture" style="max-width: 500px">
<p>Cette technique permet de réaliser à la fois l’isolation et l’étanchéité de la toiture, dans le cas où le changement du toit est nécessaire. Il permet de réduire les ponts thermiques et de garder intacts la hauteur sous combles et les éventuelles poutres apparentes. <br>Cependant, sa mise en œuvre est complexe et il est difficile de trouver un artisan (charpentier, couvreur) qui la propose. De plus, le coût de cette technique reste élevé par rapport à une isolation par l’intérieur, cette technique est donc à privilégier dans les cas où la toiture est à refaire.</p>
