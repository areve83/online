<div id="collapse2" class="panel-collapse collapse">
        <div class="panel-body"><p>Pour les constructions antérieures à 1974, <b>aucune obligation d’isoler n’était imposée.</b> Il en résulte que ces maisons étaient rarement isolées à la construction. Mais leurs performances ne sont pas toutes identiques. 
					<br>Les matériaux de construction tels que les briques et les pierres (utilisés jusqu’à la première moitié du XXe siècle) ont souvent des propriétés thermiques plus intéressantes que ceux utilisés pour les constructions des années 60 ou 70.
					<br>Pour réaliser l’isolation des maisons anciennes construites avant 1949, un diagnostic au cas par cas est indispensable pour choisir la solution d’isolation la plus appropriée. Elle tiendra compte de la nature des parois (maison en pierres, murs à remplissage, etc.).
					<br>L’isolation thermique ne doit pas entraîner de désordres ni de dégradation des éventuelles qualités du bâti, dus à un choix d’isolation inadaptée (une bonne isolation va de pair avec une bonne ventilation).</p>
					<img class="img-responsive" src="../img/deperditionsMI.png" style="border: solid 1px grey;margin: auto">
					<br>
					<p>Depuis le 1er janvier 2011, les résultats du diagnostic de performance énergétique (DPE) figurent sur les annonces immobilières (location ou vente). Vous pouvez ainsi connaître la consommation estimée pour le chauffage, l’eau chaude sanitaire et la climatisation, sur la base d’une utilisation standardisée du logement. Le DPE comprend aussi des recommandations et conseils visant à améliorer la performance énergétique.</p>
