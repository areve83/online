					<p class="pmax">VOS AVANTAGES FISCAUX</p>
					<p>La subvention de l’Anah est un revenu foncier et doit être déclarée à ce titre. Plus les loyers que vous vous engagez à pratiquer sont bas, plus la déduction fiscale sur vos revenus fonciers est importante. Elle est comprise entre 15% et 30% pour les loyers "intermédiaire" et entre 50% et 70% pour les loyers "sociaux" ou "très sociaux" uniquement si votre logement se situe dans une zone où existe un déséquilibre important. Si vous passez par une structure agréée d'intermédiation locative (Agence immobilière sociale ou association) pour louer votre logement, cette déduction peut même aller jusqu'à 85% sur l'ensemble du territoire. Cet intermédiaire peut être un organisme privé ou public. Il assure le paiement des loyers et vous garantit de récupérer votre bien en bon état.</p>
					<p><b>Prime exceptionnelle</b>
					<br>Une prime de <b>1 000 € </b>peut vous être octroyée jusqu'au 31 décembre 2022. Vous devez vous engager à confier votre logement conventionné* à une structure agréée d'intermédiation locative (Agence immobilière sociale ou association) pour une durée d’au moins 3 ans. L’intermédiation locative vous permet de déléguer et de sécuriser la gestion de votre bien.</p>
