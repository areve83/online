							<li>louer, en tant que résidence principale, à des personnes dont les ressources sont inférieures aux plafonds fixés nationalement ;</li>
							<li>ne pas louer à des personnes de votre famille proche ;</li>
							<li>remettre le bien en location en cas de départ du locataire pendant la période couverte par la convention.</li>
