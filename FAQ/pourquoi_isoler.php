			<h3 class="header-3">Pourquoi est-il important d'isoler mon logement ?</h3>
			<p>Plusieurs raisons vous conduiront à réaliser des travaux d’isolation :
			<ul>
				<li>Un bâtiment bien isolé vieillit mieux et nécessite moins de travaux d’entretien. En effet, l’isolation, combinée avec une ventilation efficace, réduit les risques de condensation qui causent souvent de nombreux désordres (peinture, fenêtres, murs).</li>
				<li>En hiver, l’isolation vous permet de réduire les déperditions de chaleur à travers les parois. En été, elle fait barrière à la chaleur extérieure. Cela entraîne immédiatement une réduction de votre facture de chauffage ou de climatisation.</li>
				<li>En hiver, les parois non isolées sont froides par contact avec l’air extérieur et provoquent des sensations d’inconfort malgré l’air chaud de la pièce. De même, les parois non isolées sont chaudes pendant la saison estivale et peuvent rendre le logement inconfortable. Une bonne isolation supprime cet effet de « paroi froide ou chaude ».</li>
				<li>L’isolation thermique permet souvent d’améliorer l’isolation acoustique d’un logement.</li>
				<li>A température égale, une maison ou un appartement isolés offrent un plus grand confort.</li>
				<li>Au moment de la vente ou de la location, votre maison bénéficie d’un meilleur classement sur l’étiquette énergie du Diagnostic de Performance Energétique (DPE). Le prix des énergies étant voué à augmenter durablement, un logement bien isolé consommera moins, coûtera moins cher à l’utilisation, et sera donc d’autant plus recherché.
				</li>
			</ul>
