<div class="panel-heading">
    <h4 class="panel-title" style="font-weight: bold;"><a data-toggle="collapse" data-parent="#accordion1" href="#collapse3">Dans le cas d'un remplacement de menuiseries, faut-il garder le dormant ?</a></h4>
</div>
<div id="collapse3" class="panel-collapse collapse">
    <div class="panel-body">
       <p>Trois méthodes peuvent être utilisées en fonction du type de menuiserie actuellement en place dans votre logement :
            <ul>
                <li><strong>Changement de fenêtre avec conservation du dormant existant</strong><br>
                <p>Rapide et sans dommage pour l’environnement immédiat de la baie (enduit, papier peint, baguette de finition, etc.), il nécessite toutefois un bon état du dormant de l’ancienne fenêtre. Il est réalisé en mettant en œuvre par recouvrement sur ce dormant une nouvelle fenêtre complète (dormant et ouvrant). Avec cette méthode, le châssis final est beaucoup plus massif. On perd en luminosité car la surface vitrée est moins grande. Il existe de plus en plus des risques de créer des ponts thermiques et phoniques si le nouveau dormant n’est pas parfaitement adapté et qu’il reste des interstices lors de l’emboîtement de l’ancien et du nouveau dormant.<p></p></li>
                <img class="img-responsive center-block" src="../img/dormant.png" style="max-width: 400px;margin-bottom: 10px;margin-top: 10px">
                <li><strong>Remplacement total de l’ancienne fenêtre</strong><br>
                <p>Opération plus lourde et plus coûteuse que la précédente, elle nécessite souvent des travaux de maçonnerie plus importants qui ne pourront préserver la décoration autour des baies. Cette méthode est généralement retenue quand des contraintes d’ordre architectural existent. Le remplacement de la fenêtre complète (ouvrant et dormant) est la solution à retenir dans tous les cas où la fenêtre existante est en mauvais état. Elle apporte une isolation thermique et acoustique supérieure. À l’usage, c’est la solution la plus performante. De plus, elle conserve la luminosité préexistante.</p></li>
                <li><strong>Ajout d'une double fenêtre</strong><br><p>Il s’agit d’ajouter à la fenêtre existante une deuxième fenêtre, posée à l’extérieur ou à l’intérieur. Pour mettre en œuvre cette technique, il faut que l’architecture du bâtiment et la configuration des pièces le permettent. Pour assurer une bonne ventilation du logement, des entrées d’air doivent être mises en place dans les deux fenêtres.</li></p>
