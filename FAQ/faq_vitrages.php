<div id="collapse2" class="panel-collapse collapse">
    <div class="panel-body"><p>La mention A-E-V vous permettra de comparer et de choisir un vitrage adapté à votre situation. Elle désigne la résistance de la menuiserie aux éléments Air, Eau et Vent. Ces trois résistances sont notées de manière croissante selon la performance de la fenêtre :
        <ul>
            <li>AIR : La perméabilité à l’air est notée de 1 (faible) à 4 (très bonne)</li>
            <li>EAU : L’étanchéité à l’eau (pluie, intempéries) est notée sur une échelle de 1A (très faible) à 9A (très bonne). Si la note est suivie de la lettre B (comme par exemple 7B), la fenêtre est sensée être installée sous une avancée de toit : seule la partie inférieure de la fenêtre a donc été testée.</li>
            <li>VENT : La résistance au vent est notée selon deux critères : la résistance à la pression (en chiffre) et la déformation de la fenêtre (en lettre). La déformation de la fenêtre est notée de A (faible) à C (très faible). La résistance à la pression du vent est notée de 1 (faible) à 5 (forte).</li>
        </ul>
