                    <h1 class="header-1">Rendu de l'opération de thermographie aérienne sur la Dracénie</h1>
                    <h2 class="header-2" style="margin-bottom: 20px;">14 et 21 avril 2018</h2>
                    <p></p>L’AREVE (Agence de Rénovation Energétique Var Est) a mené en janvier 2018 sur le territoire de la Dracénie une opération de thermographie aérienne en partenariat avec GrDF et la Communauté d’Agglomération Dracénoise.
                    <br>Cette opération consiste à <b>relever par avion et à l’aide d’une caméra thermique les principales déperditions de chaleur de l’ensemble du bâti</b> sur une zone considérée. 
                    <br>L’opération a couvert les villes suivantes : 
                    <ul>
                        <li>Salernes</li>
                        <li>Flayosc</li>
                        <li>Draguignan</li>
                        <li>Lorgues</li>
                        <li>Trans en Provence</li>
                        <li>la Motte</li>
                        <li>les Arcs</li>
                        <li>Le Muy</li>
                        <li>Vidauban</li>
                    </ul>
                    Les premiers rendus de cette thermographie auront lieu :
                    <ul>
                        <li><b>Le samedi 14 avril de 10h à 17h en mairie de La Motte </b>(prioritairement pour les habitants de Trans en Provence, Le Muy, Les Arcs, Vidauban, Draguignan et la Motte)</li>
                        <li><b>Le samedi 21 avril de 10h à 17h en mairie de Flayosc</b>, salle Mistral (prioritairement pour les habitants de Salernes, Lorgues, Draguignan et Flayosc)</li>
                    </ul>
                    Les propriétaires qui ne pourront pas se déplacer ces deux jours auront l’occasion de se rattraper le 12 juin à Draguignan (lieu à définir).
                    <br>D’autres dates seront bientôt annoncées.
                    <br>Seront également associés à ces rendus des entreprises partenaires de l'AREVE, professionnels locaux spécialisés dans l’isolation qui pourront échanger avec les habitants et présenter leurs offres.
