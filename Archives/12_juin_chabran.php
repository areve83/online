                    <h3 class="header-3">Rendu de l'opération de thermographie aérienne sur la Dracénie</h3>
                    <p>L’AREVE (Agence de Rénovation Energétique Var Est) a mené en janvier 2018 sur le territoire de la Dracénie une opération de thermographie aérienne en partenariat avec GrDF et la Communauté d’Agglomération Dracénoise.
